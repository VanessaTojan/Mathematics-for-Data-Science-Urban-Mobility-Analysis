import numpy as np
import matplotlib.pyplot as plt

A = np.array([
    [2, -1, 1, 0],
    [1, 1, 0, 1],
    [-1, 0, 3, -1],
    [0, 1, 1, 1]
], dtype=float)

B = np.array([150, 420, 100, 380], dtype=float)

X = np.linalg.solve(A, B)

print("Traffic flow solution:")
print("x1 =", X[0])
print("x2 =", X[1])
print("x3 =", X[2])
print("x4 =", X[3])

det_A = np.linalg.det(A)
print("Determinant of A =", det_A)

labels = ["x1", "x2", "x3", "x4"]

plt.bar(labels, X)
plt.title("Equilibrium Traffic Flows")
plt.xlabel("Road Link")
plt.ylabel("Vehicles per 10 minutes")
plt.show()

